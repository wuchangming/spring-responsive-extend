package com.woqu.wap.responsive.browser;
/**
 * Enumeration for the type of browser that has been resolved
 * @author WuChangming
 *
 */
public enum BrowserType {
	
	/**
	 * Represents a normal browser.
	 */
	NORMAL,
	/**
	 * Represents a iOS APP browser.
	 */
	WOQU_IOS_APP,
	/**
	 * Represents a android APP browser.
	 */
	WOQU_ANDROID_APP,
	/**
	 * Represents a WeiXin browser. 
	 */
	WEIXIN
}
